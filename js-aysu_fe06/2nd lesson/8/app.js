let x = 4;
let y = 8;

let operator = prompt('the numbers are 4 and 8 pick an operator')

switch(operator){
    case '+' :
        console.log(x + y);
        break;
    case '-' :
        console.log(x - y);
        break;
    case '*' :
        console.log(x * y);
        break;
    case '/' :
        console.log(x / y);
        break;
}



